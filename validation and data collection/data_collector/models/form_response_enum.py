    RESPONSE_ID = "response_id"
    RESPONSE_FAMILY_ID = "response_family_id"
    USER_ID = "user_id"
    USERNAME = "username"
    LOCATION = "location"
    SUBMITTED_TIME = "submitted_time"
    SYNCED_TIME = "synced_time"
    LAST_MODIFIED_TIME = "last_modified_time"

